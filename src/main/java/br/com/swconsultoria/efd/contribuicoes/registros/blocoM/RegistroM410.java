package br.com.swconsultoria.efd.contribuicoes.registros.blocoM;

/**
 * @author Yuri Lemes
 *
 */
public class RegistroM410 {

	private final String reg = "M410";
	private String nat_rec;
	private String vl_rec;
	private String cod_cta;
	private String desc_compl;

	/**
	 * @return the nat_rec
	 */
	public String getNat_rec() {
		return nat_rec;
	}

	/**
	 * @param nat_rec
	 *            the nat_rec to set
	 */
	public void setNat_rec(String nat_rec) {
		this.nat_rec = nat_rec;
	}

	/**
	 * @return the vl_rec
	 */
	public String getVl_rec() {
		return vl_rec;
	}

	/**
	 * @param vl_rec
	 *            the vl_rec to set
	 */
	public void setVl_rec(String vl_rec) {
		this.vl_rec = vl_rec;
	}

	/**
	 * @return the cod_cta
	 */
	public String getCod_cta() {
		return cod_cta;
	}

	/**
	 * @param cod_cta
	 *            the cod_cta to set
	 */
	public void setCod_cta(String cod_cta) {
		this.cod_cta = cod_cta;
	}

	/**
	 * @return the desc_compl
	 */
	public String getDesc_compl() {
		return desc_compl;
	}

	/**
	 * @param desc_compl
	 *            the desc_compl to set
	 */
	public void setDesc_compl(String desc_compl) {
		this.desc_compl = desc_compl;
	}

	/**
	 * @return the reg
	 */
	public String getReg() {
		return reg;
	}

}
